    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(txtCodigo,"REGISTRE ESTUDIANTE A ELIMINAR", "mesaje del sistema", HEIGHT);
        }else{
            int index = Lista_Estudiantes.BuscarAlumno(txtCodigo.getText());
          if(index !=-1){
            TLista datos=Lista_Estudiantes.obtener();
            Estudiante doc =  (Estudiante) datos.Obtener(index); 
            txtNombre.setText(doc.getNombre());
            txtApellido.setText(doc.getApellidos());
            txtDocumento.setText(doc.getDocumentoIdentidad());
            String tipo = (String) cboTipoDocumento.getSelectedItem();
            if(tipo.equalsIgnoreCase("DNI"))
                cboTipoDocumento.setSelectedIndex(0);
            else
                cboTipoDocumento.setSelectedIndex(1);
            txtTelefono.setText(String.valueOf(doc.getTelefono()));
            String sexo = (String) cboSexo.getSelectedItem();
            if(sexo.equalsIgnoreCase("masculino"))
                cboSexo.setSelectedIndex(1);
            else
                cboSexo.setSelectedIndex(2);
            
             int desicion = JOptionPane.showConfirmDialog(txtCodigo, "¬øDESEA ELIMINAR EL ESTUDIANTE?", "mensaje del sistema", WIDTH);
                if(desicion == JOptionPane.YES_OPTION)
                {
                    Lista_Estudiantes.eliminar(txtCodigo.getText());
                     txtCodigo.setText("");
                     txtDocumento.setText("");
                      txtNombre.setText("");
                     txtApellido.setText("");
                     txtTelefono.setText("");
                     txtNacionalidad.setText("");
                     txtCodigo.requestFocus();
                     cboSexo.setSelectedIndex(0);
                     cboTipoDocumento.setSelectedIndex(0);
                     JOptionPane.showMessageDialog(txtCodigo, "ESTUDIANTE ELIMINADO", "mesaje del sistema", WIDTH);
                }
          }
          else
              JOptionPane.showMessageDialog(txtCodigo, "ESTUDIANTE NO ENCONTRADO", "mesaje del sistema", WIDTH);
        }
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed


