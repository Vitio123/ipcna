public DefaultTableModel tabla(){
    DefaultTableModel tablaAlum = new DefaultTableModel();
    TLista datos= Lista_Estudiantes.obtener();
    tablaAlum.addColumn("CODIGO");
    tablaAlum.addColumn("NOMBRE");
    tablaAlum.addColumn("APELLIDOS");
    tablaAlum.addColumn("DOCUMENTO DE IDENTIDAD");
    tablaAlum.addColumn("NACIONALIDAD");
    tablaAlum.addColumn("SEXO");
    tablaAlum.addColumn("TELEFONO");
    Object[] arreglo = new Object [7];
    for (int i = 0; i < datos.Cantidad(); i++) {
        Estudiante E = (Estudiante) datos.Obtener(i);
        arreglo[0]=E.getCodigoEstudiante();
        arreglo[1]=E.getNombre();
        arreglo[2]=E.getApellidos();
        arreglo[3]=E.getDocumentoIdentidad();
        arreglo[4]=E.getNacionalidad();
        arreglo[5]=E.getSexo();
        arreglo[6]=E.getTelefono();
        tablaAlum.addRow(arreglo);
    }
    
return tablaAlum;}
